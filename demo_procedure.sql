-- DAFTAR
DECLARE
  USRID CHAR(10);
  KMTID CHAR(10);
BEGIN
  USRID := 'USR0000050';
  KMTID := 'KMT0000001';

  DAFTAR(
    USRID => USRID,
    KMTID => KMTID
  );
END;

